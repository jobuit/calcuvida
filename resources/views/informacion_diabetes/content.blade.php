            <h1 class="display-5 mb-3">¿Qué es la diabetes?</h1>
            <p class="blockquote">La diabetes es una enfermedad que se presenta cuando el nivel de glucosa en la sangre,
                también conocido como azúcar en la sangre, es demasiado alto. La glucosa en la sangre
                es la principal fuente de energía y proviene de los alimentos. La insulina, una hormona
                que produce el páncreas, ayuda a que la glucosa de los alimentos ingrese en las células
                para usarse como energía. Algunas veces, el cuerpo no produce suficiente o no produce
                nada de insulina o no la usa adecuadamente y la glucosa se queda en la sangre y no llega a las células.</p>
            <p class="blockquote">Con el tiempo, el exceso de glucosa en la sangre puede causar problemas de salud. Aunque la diabetes no tiene cura,
                la persona con diabetes puede tomar medidas para controlar su enfermedad y mantenerse sana.</p>
            <p class="blockquote">A veces las personas cuando tiene diabetes dicen que tienen “un poquito alto el azúcar” o que tienen “prediabetes”. Estos términos nos hacen pensar que la persona realmente no tiene diabetes o que su caso
                es menos grave. Sin embargo, todos los casos de diabetes son graves.</p>
            <h2 class="display-6 mb-3" id="seccion1">Los principales tipos de diabetes son tipo 1, tipo 2 y la diabetes gestacional.</h2>
            <h4 class="" >Diabetes gestacional.</h4>
            <p class="blockquote">La diabetes gestacional es otro tipo común de diabetes. Es una condición temporal que se produce durante el embarazo.</p>
            <p class="blockquote">Exigencias adicionales al páncreas causan en algunas mujeres el desarrollo de diabetes durante el embarazo. A menudo, desaparece
                después del parto. Pero, más tarde en la vida, la diabetes puede volver.</p>
            <p class="blockquote">La diabetes gestacional afecta entre 2% y 4% de todos los embarazos, con un aumento del riesgo de desarrollar diabetes, tanto para la madre como para el niño.</p>
            <p class="blockquote">El riesgo de desarrollar diabetes tipo 2 es mayor si la madre ha dado a luz a un bebé que pesó más de 4 kg (9 libras) al nacer.
                El tratamiento consistirá en seguir una dieta saludable, la actividad física y, en algunos casos, terapia con insulina.</p>

            <h4 id="seccion2" class="mt-5 mb-3">Diabetes tipo 1 mellitus.</h4>
            <p class="blockquote">La diabetes tipo 1 se desarrolla cuando una "reacción autoinmune" destruye las células beta en el páncreas. Reacción autoinmune significa que el cuerpo crea anticuerpos contra sus propias células. Como resultado,
                el páncreas deja de producir insulina o no puede producir suficiente insulina por sí solo.</p>
            <p class="blockquote">La diabetes tipo 1 se desarolla con mayor frequencia las personas menores de 20 años. Otro termino</p>
            <p class="blockquote">La diabetes tipo 1 es una enfermedad de por vida que se trata con inyecciones de insulina. Las inyecciones deben administrarse cada día y algunas personas requieren varias inyecciones al día para ayudar
                a mantener el control de glucosa en sangre junto con una alimentación saludable y ejercicio regular.</p>
            <p class="blockquote">La diabetes tipo 1 se desarolla con mayor frequencia en las personas menores de 20 años. Otro termino utilizado previamente para
                la diabetes tipo 1 es diabetes mellitus insulinodependiente (DMID) de comienzo juvenil. </p><br>
            <h4 id="seccion3" class="mb-3">Diabetes tipo 2 mellitus.</h4>
            <p class="blockquote">La diabetes tipo 2 es un término para varios trastornos con diferentes causas y grados de gravedad. Es el tipo más común de diabetes.</p>
            <p class="blockquote">A menudo, las personas con diabetes tipo 2 todavía puede hacer su propia insulina en el páncreas, pero en cantidad insuficiente o la insulina que se produce no se utiliza tan eficazmente por el cuerpo y no se puede mover la glucosa a las células del cuerpo. Esto provoca que la glucosa se acumule en la sangre y puede causar hiperglicemia (niveles altos de azúcar en la sangre). Cuando la insulina no funciona tan eficazmente como normal, también se le llama "reduccion de la sensibilidad a la insulina" o "resistencia a la insulina".</p>
            <p class="blockquote">Muchas personas manejan la diabetes tipo 2 simplemente siguiendo una dieta saludable y ejercicio regular. En los individuos con sobrepeso u obesidad, diabetes tipo 2 a menudo mejora como resultado de la pérdida de peso, una dieta saludable y ejercicio.
                Con la progresión de la enfermedad, algunas personas pueden tener que tomar medicamentos orales o inyecciones de insulina.
                Otros términos utilizados previamente para la diabetes tipo 2 son la diabetes del adulto y diabetes mellitus no insulino-dependiente (DMNID).</p><br>
            <h2 id="seccion3" class="mb-3">Sintomas de la diabetes.</h2>
            <p class="blockquote"><strong>1. El exceso de orina:</strong> Es una de las primeras señales y síntomas de la diabetes. Cuando hay una elevada concentración de glucosa en la sangre, generalmente arriba de 180mg/dl.</p>
            <p class="blockquote"><strong>2. Sed excesiva:</strong> Si el paciente diabético orina en exceso, perderá más agua de lo previsto, quedando deshidratado. La sed es el principal mecanismo de defensa del organismo contra la deshidratación.</p>
            <p class="blockquote">El exceso de glucosa aumenta la cantidad de agua perdida en la orina, con lo cual el paciente orina con mucha frecuencia.</p>
            <p class="blockquote"><strong>3. Perdida de peso:</strong> La pérdida de peso es un síntoma muy común en la diabetes tipo 1. Puede también ocurrir en la diabetes tipo 2, pero no es tan frecuente.</p>
            <p class="blockquote">La insulina también es la hormona responsable por el almacenamiento de grasa y por la síntesis de proteínas en el organismo. Como en la diabetes tipo 1 hay ausencia de insulina, el paciente deja de almacenar grasa y de producir músculos. </p>
            <p class="blockquote"><strong>4. Hambre excesiva:</strong> El cuerpo interpreta este hecho como si el paciente estuviese en ayunas. El organismo necesita de energía y la única forma que tiene para obtenerla es a través de la alimentación..</p>
            <p class="blockquote">En la diabetes tipo 1 inicialmente hay un aumento del hambre, pero en fases más avanzadas el paciente se torna anoréxico, lo que contribuye aún más a la pérdida de peso.</p>
            <p class="blockquote"><strong>5. Vision borrosa:</strong> El exceso de glucosa en la sangre causa una inflamación del cristalino, la lente del ojo, cambiando su forma y flexibilidad, disminuyendo la capacidad de foco, lo que torna la visión borrosa.</p>
            <p class="blockquote"><strong>6. Cetoacidosis diabética:</strong> Es una complicación de la diabetes tipo 1, siendo muchas veces la primera señal de la enfermedad. Como hay ausencia de insulina, las células no reciben glucosa y necesitan encontrar otra fuente para generar energía.</p>
            <p class="blockquote">La cetoacidosis diabética suele ocurrir cuando los niveles de glucosa en la sangre sobrepasan los 500mg/dl y es una emergencia médica porque hace que el pH de la sangre caiga a niveles peligrosos, pudiendo llevar a la muerte. Las señales y los síntomas de la
                cetoacidosis son náuseas, vómitos, dolor abdominal, confusión mental, postración y dificultad respiratoria</p>





