            <p class="blockquote">La diabetes gestacional consiste en una alteración metabólica de los hidratos de carbono
                o alteración de la tolerancia a la glucosa que se diagnostica por primera vez durante la gestación.
                Por ello, al tratarse de una enfermedad que surge durante el embarazo, no sólo afecta a la madre, sino también al bebé.</p>
            <p class="blockquote mb-3">
                Esta patología puede llegar a aparecer por la mala adaptación de los mecanismos reguladores de los carbohidratos en la madre.
                Esto se produce porque “las hormonas producidas durante el embarazo por la placenta bloquean la actividad de la insulina,
                que es la sustancia encargada de regularizar el consumo de glucosa”, apunta José María Fernández Moya, coordinador del
                servicio de Ginecología y Obstetricia del Hospital Nisa Pardo de Aravaca, en Madrid.
            </p>
            <h2 class="mb-3 mt-5">Pautas para controlar la diabetes durante el embarazo</h2>
            <p class="blockquote">Para poder evitar padecerla, tanto en las madres como en sus futuros hijos, hay que acudir al médico para iniciar un plan
                de tratamiento y poder controlar la diabetes durante el embarazo. Es muy importante seguir los consejos del profesional
                médico y acudir a las consultas de forma rutinaria.
            </p>
            <p class="blockquote">
                Una dieta equilibrada es el elemento clave para conseguir que la diabetes desaparezca. El plan de alimentación debe ser personalizado
                para cada paciente y adaptarse a sus necesidades. En ningún caso es recomendable la bajada de peso al seguir este tipo de dieta,
                no se trata de adelgazar sino de basar la nutrición en alimentos saludables
            </p>
            <p class="blockquote">
                Lo mejor es consultar con un nutricionista o educador en diabetes para que la embarazada pueda aprender cuáles son alimentos que debe
                consumir para tener el nivel de glucosa bajo control. Este plan dietético no debe ser ni hipocalórico ni restrictivo en carbohidratos,
                se recomienda el consumo de frutas, pasta y arroz, pero sí se controlan los productos con azúcar refinado, las proteínas y las grasas.
            </p>
            <p class="blockquote">
                El número de comidas y las calorías diarias tienen que estar bien distribuidas y fraccionadas para evitar hipoglucemias.
            </p>
            <p class="blockquote">
                Otra de las pautas a seguir es realizar ejercicio físico diario pero moderado, siempre acorde con las indicaciones médicas.
                Un paseo diario o ejercicios de las extremidades superiores y actividades aeróbicas un par de veces por semana son válidos.
                Esto ayuda a mejorar en el aumento de la sensibilidad a la insulina y el consumo de glucosa.
            </p>
            <p class="blockquote">
                Por último, como tratamiento adicional está la toma de medicamentos. Aunque esto no esté pautado en todos los casos.
                La insulina resulta ser el más eficaz para la diabetes, pero no es la misma actuación cuando se tiene durante la gestación.
                Por lo que siempre se debe cumplir con las instrucciones médicas.
            </p>
            <h2 class="mt-5">Factores de riesgo</h2>
            <p class="blockquote">
                Existen algunos factores de riesgo que pueden ayudar a identificar y prever si una mujer puede ser diabética gestacional,
                lo que ayudará no sólo a evitarlo sino a saber cómo tratarlo:
            </p>
            <ul>
                <li><p class="lead">Antecedentes de familiares con primer grado de diabetes.</p></li>
                <li><p class="lead">Haber padecido diabetes o cualquier otro problema con la glucosa con anterioridad.</p></li>
                <li><p class="lead">Haber padecido preeclampsia, síndrome de ovario poliquístico (SOP) o hipertensión.<P></li>
                <li><p class="lead">Tener una edad entre los 25 y 30 años.</p></li>
                <li><p class="lead">Tener un índice de masa corporal (IMC) superior a 25-30, dependiendo del tiempo de gestación, es decir, padecer sobrepeso.</p></li>
                <li><p class="lead">Poseer antecedentes de recién nacido con macrosomía, esto es, el tamaño o desarrollo excesivo del cuerpo.</p></li>
                <li><p class="lead">Antecedentes de la madre de bajo o alto peso al nacer.</p></li>
                <li><p class="lead">Antecedentes de aborto espontáneo o mortalidad del feto al nacer.</p></li>
